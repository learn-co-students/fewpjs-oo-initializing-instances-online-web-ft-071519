// breakfast
// has a class of breakfast
class Breakfast {
    // creates a new breakfast with the correct parameters
    constructor(food, drink) {
        this.food = food;
        this.drink = drink;
    }
}

// lunch
// has a class of lunch
class Lunch {
    // creates a new lunch with the correct parameters
    constructor(salad, soup, drink) {
        this.salad = salad;
        this.soup = soup;
        this.drink = drink;
    }
}

// dinner
// has a class of dinner
class Dinner {
    // creates a new dinner with the correct parameters
    constructor(salad, soup, entree, dessert) {
        this.salad = salad;
        this.soup = soup;
        this.entree = entree;
        this._dessert = dessert;
    }
}

